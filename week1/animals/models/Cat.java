package animals.models;

public class Cat{
  public String name;
  public String breed;
  public int age;
  public boolean isMale;
  public String color;

    public Cat(String name, String breed, int age, boolean isMale, String color){
      this.name = name;
      this.breed = breed;
      this.age = age;
      this.isMale = isMale;
      this.color = color;
    }
}